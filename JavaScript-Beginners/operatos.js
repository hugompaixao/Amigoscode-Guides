// Comparison Operators
// < <= > >= == !=
// boolean is either true or false
console.log(10 == 10)
console.log(10 != 10)
console.log(10 < 10)
console.log(10 <= 10)
console.log(10 > 10)
console.log(10 >= 10)

console.log("A" == "B")
console.log("A" != "B")

// Logical Operators
// && ||
console.log(10 == 10 && "A" == "A")
console.log(10 == 10 || "A" == "B")