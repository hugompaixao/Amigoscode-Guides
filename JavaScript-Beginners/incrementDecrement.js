// Increment and Decrement Operators
var number = 0;
// Postfix
console.log("Postfix")
console.log("Increment")
console.log(number++)
console.log(number)
console.log("Decrement")
console.log(number--)
console.log(number)

console.log();
// Prefix
console.log("Prefix")
var numberTwo = 0;
console.log("Increment")
console.log(++numberTwo);
console.log("Decrement")
console.log(--numberTwo);

console.log("for loop")
for (var i = 0; i <= 10; i++) {
    console.log(i);
}